

let num1 = 10;
let num2 = 8;


// Addition (+): Adds two values together.
console.log(`The addition of numi and num2 is ${num1 +   num2}`);


// Subtraction -): Subtracts one value from another.
console.log(`The subtraction of num1 and num2 is ${num1 - num2}`);


// Multiplication (*): Multiplies two values together.
console.log(`The multiplication of num1 and num2 is ${num1 * num2}`);


// Division (/): Divides one value by another.
console.log(`The division of num and num2 is ${num1 / num2}`);


// Modulus (%): Returns the remainder of a division operation.
console.log(`The result of modulo operation of num and num2 is  ${num1 % num2}`);


// Exponentiation(**): raises to the power of.
console.log(`The exponential of numi and num2 is ${num1 ** num2}`);


